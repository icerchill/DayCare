package edu.neu.csye6200;

public class Person {
	private String Name;
	private int age;
	//getters and setters
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	//constructor
	public Person() {
		super();
		
	}
	

}
